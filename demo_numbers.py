#This script is to demonstrate numbers.

#Defining, initializing Integer numbers.
i = 5; j = 10
print('Value of i = ', i, '\tValue of j = ' , j, '\tValue of i+j =' , i+j)

#Defining, initializing Floating numbers.
i = 5.1; j = 11.2
print('Value of i = ', i, '\tValue of j = ' , j, '\tValue of i+j =' , round(i+j, 2))

#Converting Interger to Floating Point Number and vice versa using inbuilt functions.
a = 6; b = 20.1234
print('Value of Integer a in Floating Point is: ', float(a))
print('Value of Float b in Integer is: ', int(b))

